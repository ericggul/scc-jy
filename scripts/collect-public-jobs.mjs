const targetCount = 1_000;

function workplace(location) {
  const value = location.toLowerCase();
  if (/(remote|anywhere|worldwide|work from home)/.test(value)) return "Remote";
  if (/hybrid/.test(value)) return "Hybrid";
  return "On-site";
}

function cleanTeam(job) {
  return job.departments?.map((department) => department.name).filter(Boolean).join(" · ")
    || job.offices?.map((office) => office.name).filter(Boolean).join(" · ")
    || "Product & Technology";
}

function decodeEntities(value) {
  return value
    .replaceAll("&amp;", "&")
    .replaceAll("&nbsp;", " ")
    .replaceAll("&quot;", '"')
    .replaceAll("&#39;", "'")
    .replaceAll("&lt;", "<")
    .replaceAll("&gt;", ">");
}

function descriptionExcerpt(content) {
  const text = decodeEntities(content || "")
    .replace(/<\/(p|div|li|h[1-6]|br|ul|ol)>/gi, "\n")
    .replace(/<[^>]+>/g, " ")
    .replace(/\s*\n\s*/g, "\n")
    .replace(/[ \t]+/g, " ")
    .trim();
  if (!text) return "See the company careers page for the complete job description.";
  const boundary = text.slice(0, 900).lastIndexOf(". ");
  return text.slice(0, boundary > 350 ? boundary + 1 : 900).trim();
}

async function getBoard({ board, company, take }) {
  const response = await fetch(`https://boards-api.greenhouse.io/v1/boards/${board}/jobs?content=true`);
  if (!response.ok) throw new Error(`Unable to fetch ${company}: ${response.status}`);
  const data = await response.json();
  const jobs = [...data.jobs]
    .sort((a, b) => new Date(b.updated_at).getTime() - new Date(a.updated_at).getTime())
    .slice(0, take)
    .map((job) => {
      const location = job.location?.name || "Location not specified";
      return {
        id: `${board}-${job.id}`,
        sourceId: String(job.id),
        title: job.title,
        company,
        location,
        workplace: workplace(location),
        employment: "Full-time",
        team: cleanTeam(job),
        postedAt: job.updated_at,
        description: descriptionExcerpt(job.content),
        sourceBoard: `${company} public Greenhouse board`,
        applicationUrl: job.absolute_url,
      };
    });
  return { company, available: data.jobs.length, jobs };
}

const settled = await Promise.allSettled(boards.map(getBoard));
const failures = settled
  .filter((result) => result.status === "rejected")
  .map((result) => result.reason.message);
const results = settled
  .filter((result) => result.status === "fulfilled")
  .map((result) => result.value);
const jobs = Array.from(
  { length: Math.max(...results.map((result) => result.jobs.length)) },
  (_, index) => results.flatMap((result) => result.jobs[index] ? [result.jobs[index]] : []),
).flat().slice(0, targetCount);
if (jobs.length < targetCount) throw new Error(`Only ${jobs.length} public jobs were collected. ${failures.join(" | ")}`);
const output = `// Generated by scripts/collect-public-jobs.mjs on ${new Date().toISOString()}.\n// Sources: public Greenhouse boards. Do not represent these records as LinkedIn-hosted postings.\n\nimport type { LinkedinJob } from "./job-types";\n\nexport const linkedinJobs: LinkedinJob[] = ${JSON.stringify(jobs, null, 2)};\n`;
const outputPath = resolve("components/sns/linkedin/1/model/jobs.generated.ts");
await mkdir(dirname(outputPath), { recursive: true });
await writeFile(outputPath, output);

